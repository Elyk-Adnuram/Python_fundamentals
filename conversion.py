'''
Create variables of different data types
Convert each variable to another data type
Print the variables on separate lines
'''
# Declaring variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

# Cast above variables to new data types
num1 = int(99.23)
num2 = float(23)
num3 = str(150)
string1 = int("100")

# Print the variables
print(type(num1))
print(type(num2))
print(type(num3))
print(type(string1))
